import Head from "next/head";
import Hero from "@/components/home/Hero";
import { Container } from "@/layouts/Container";
import CardList from "@/components/card/CardList";
import Title from "@/components/title/Title";
import { ArticleProps, Category } from "@/type";
import { sanityClient } from "@/studio/client";
import Meta from "@/components/Meta";
import PjCardList from "@/components/card/PjCardList";

interface HomeProps {
  blogs: ArticleProps[];
  projects: ArticleProps[];
  snippets: ArticleProps[];
  categories: Category[];
}

const query = `{
  "blogs":*[_type == "blog"][0..2]{
  title,
    slug,
    _id,
    _type,
    body,
    'mainImage':mainImage.asset->url,
    description,
        publishedAt,
    author->{
      name
    }, 
    "readingTime": round(length(pt::text(body)) / 5 / 240 ),
},

"projects":*[_type == "project"][0..2]{
  title,
    slug,
    _id,
    _type,
    body,
    'mainImage':mainImage.asset->url,
    description,
        publishedAt,
    
    author->{
      name
    },
    "readingTime": round(length(pt::text(body)) / 5 / 240 ),
},

"snippets":*[_type == "snippet"][0..2]{
  title,
    slug,
    _id,
    _type,
    body,
    'mainImage':mainImage.asset->url,
    description,
        publishedAt,
    
    author->{
      name
    },
    "readingTime": round(length(pt::text(body)) / 5 / 240 ),
},
  "categories":*[_type=="category"]
}`;

export default function Home(props: HomeProps) {
  const { blogs, projects, snippets } = props;
  return (
    <>
      <Head>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <div>
        <Container>
          <Meta
            meta={{
              title: "Home| Zaw Z Tun",
              image: "/zzt.jpeg",
              description:
                "I am FullStack Engineer. My background is Cisco certified Network Engineer.",
            }}
          ></Meta>

          <Hero />
          <Title href="/projects" title="Latest Projects"></Title>
          <PjCardList blogs={projects} />
          <Title href="/blogs" title="Latest Blogs"></Title>
          <CardList blogs={blogs} />
          <Title href="/snippets" title="Latest Snippets"></Title>
          <CardList blogs={snippets} />
        </Container>
      </div>
    </>
  );
}

export async function getStaticProps() {
  const data = await sanityClient.fetch(query);

  return {
    props: {
      ...data,
    },
    revalidate: 10,
  };
}
